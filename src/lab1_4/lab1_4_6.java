package lab1_4;

public class lab1_4_6 {
    public static void main(String[] args) {
        int a = 21;
        int b = 13;
        System.out.println("a & b = " + (a & b));
        System.out.println("a | b = " + (a | b));
        System.out.println("a ^ b = " + (a ^ b));
        System.out.println("!a = " + ~a);
        System.out.println("!b = " + ~b);
    }
}
