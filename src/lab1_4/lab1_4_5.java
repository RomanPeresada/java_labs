public class lab1_4_5 {
    public static void main(String[] args) {
        int a = 10, b = 10, c = 100;
        int a1 = a << 2;
        int a2 = a << 1;
        int b1 = b >> 3;
        int c1 = c >>> 4;
        System.out.println("10 << 2 = " + a1);
        System.out.println("10 <<1 = " + a2);
        System.out.println("10 >> 3 = " + b1);
        System.out.println("100 >>> 2 = " + c1);

    }
}
